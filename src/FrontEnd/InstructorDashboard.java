    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String courseId = JOptionPane.showInputDialog(this, "Enter the Course ID to view students for:");
        Course courseToView =FindCourseById(courseId);
        if (courseToView == null) {
            return; 
        }
        List<Student> students = controller.getEnrolledStudents(courseToView.getCourseId());
        if (students.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No students are currently enrolled in this course.");
            return;
        }
        StringBuilder studentList = new StringBuilder("Enrolled Students for '" + courseToView.getTitle() + "':\n\n");
        for (Student student : students) {
            studentList.append("- ").append(student.getUsername()).append("\n");
        }
        JOptionPane.showMessageDialog(this, studentList.toString());

    
    }//GEN-LAST:event_jButton5ActionPerformed

