    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
    String title = JOptionPane.showInputDialog(this, "Enter new course title:");
    if (title == null || title.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Title cannot be empty.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    String description = JOptionPane.showInputDialog(this, "Enter course description:");
    if (description == null) {
        return; 
    }
    
    try {
        boolean success = controller.createCourse(title, description, currentI);
        if (success) {
            JOptionPane.showMessageDialog(this, "Course created successfully!");
        } else {
            JOptionPane.showMessageDialog(this, 
                "Error: Could not create course. Check console for details.", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, 
            "Unexpected error: " + e.getMessage(), 
            "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
}                                        
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String courseId = JOptionPane.showInputDialog(this, "Enter the Course ID to delete:");
        Course  courseToDelete=FindCourseById(courseId);
        if(courseToDelete==null){
            return;
        }
        int choice = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete '" + courseToDelete.getTitle() + "'?",
            "Confirm Deletion",
            JOptionPane.YES_NO_OPTION
        );
        
        if (choice == JOptionPane.YES_OPTION) {
            boolean success = controller.deleteCourse(courseToDelete.getCourseId(), currentI);
            
            if (success) {
                JOptionPane.showMessageDialog(this, "Course deleted.");
            } else {
                JOptionPane.showMessageDialog(this, "Error: Course could not be deleted.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String courseId = JOptionPane.showInputDialog(this, "Enter the Course ID to edit:");
        Course courseToEdit=FindCourseById(courseId);
        if(courseToEdit==null){
            return;
        }
        String newTitle = JOptionPane.showInputDialog(this, "Enter the new title:", courseToEdit.getTitle());
    
        if (newTitle == null || newTitle.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Title cannot be empty.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String newDescription = JOptionPane.showInputDialog(this, "Enter the new description:", courseToEdit.getDescription());
        if (newDescription == null) {
            return;
        }
    
        boolean success = controller.editCourse(courseId, newTitle, newDescription);
        
        if (success) {
            JOptionPane.showMessageDialog(this, "Course updated.");
        } else {
            JOptionPane.showMessageDialog(this, "Error: Course could not be updated.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    
    
    
    }//GEN-LAST:event_jButton2ActionPerformed

