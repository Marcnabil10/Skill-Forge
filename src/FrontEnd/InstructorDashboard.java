    private Course FindCourseById(String courseId){
        if(courseId==null||courseId.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,"Id is empty");
            return null;
        }
        Course course = controller.getCourseById(courseId);
      if (course == null) {
            JOptionPane.showMessageDialog(this, "Error: Course not found.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        if (!course.getInstructorId().equals(currentI.getUserId())) {
            JOptionPane.showMessageDialog(this, "Error: You do not own this course.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        
        return course; 
    }
    

